// FALSY VALUES IN JS are 
//  false, null, undefined, 0 and ''
let bool = false;
if(!bool) {
    console.log("bool is falsy");
}

bool = null;
if(!bool) {
    console.log("bool is falsy");
}

bool = undefined;
if(!bool) {
    console.log("bool is falsy");
}

bool = 0;
if(!bool) {
    console.log("bool is falsy");
}

bool = '';
if(!bool) {
    console.log("bool is falsy");
}
